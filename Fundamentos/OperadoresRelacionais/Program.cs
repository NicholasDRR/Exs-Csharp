Console.WriteLine("Operadores relacionais!");

int x = 10;
int y = 20;

System.Console.WriteLine($"\nX == Y: {x==y}");
System.Console.WriteLine($"X > Y: {x>y}");
System.Console.WriteLine($"X < Y: {x<y}");
System.Console.WriteLine($"X >= Y: {x>=y}");
System.Console.WriteLine($"X <= Y: {x<=y}");
System.Console.WriteLine($"X != Y: {x!=y}\n");

string nomeMinusculo = "kond";
string nomeMaiusculo = "Kond";

